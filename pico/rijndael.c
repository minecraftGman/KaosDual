/* GF(2^8) multiply by 2 */
#define XTIME(x) ( ((x)<<1) ^ (((x)&0x80) ? 0x1b : 0x00) )
#define MUL(x,y) ( ((y)&1)*(x) ^ ((y)>>1&1)*XTIME(x) ^ ((y)>>2&1)*XTIME(XTIME(x)) \
                 ^ ((y)>>3&1)*XTIME(XTIME(XTIME(x))) ^ ((y)>>4&1)*XTIME(XTIME(XTIME(XTIME(x)))) )

static const uint32_t rcon[] = {
    0x01000000,0x02000000,0x04000000,0x08000000,0x10000000,
    0x20000000,0x40000000,0x80000000,0x1b000000,0x36000000
};

static uint32_t subword(uint32_t w) {
    return ((uint32_t)FSb[w>>24]<<24)|((uint32_t)FSb[(w>>16)&0xff]<<16)
          |((uint32_t)FSb[(w>>8)&0xff]<<8)|(uint32_t)FSb[w&0xff];
}
static uint32_t rotword(uint32_t w) { return (w<<8)|(w>>24); }

int rijndaelKeySetupEnc(uint32_t rk[], const uint8_t key[], int bits) {
    int i = 0;
    rk[0]=GETU32(key); rk[1]=GETU32(key+4); rk[2]=GETU32(key+8); rk[3]=GETU32(key+12);
    for(;;) {
        rk[4] = rk[0] ^ subword(rotword(rk[3])) ^ rcon[i];
        rk[5] = rk[1] ^ rk[4];
        rk[6] = rk[2] ^ rk[5];
        rk[7] = rk[3] ^ rk[6];
        if(++i == 10) return 10;
        rk += 4;
    }
    (void)bits;
}

int rijndaelKeySetupDec(uint32_t rk[], const uint8_t key[], int bits) {
    int Nr = rijndaelKeySetupEnc(rk, key, bits), i, j;
    uint32_t tmp;
    for(i=0,j=4*Nr; i<j; i+=4,j-=4) {
        tmp=rk[i];   rk[i]=rk[j];   rk[j]=tmp;
        tmp=rk[i+1]; rk[i+1]=rk[j+1]; rk[j+1]=tmp;
        tmp=rk[i+2]; rk[i+2]=rk[j+2]; rk[j+2]=tmp;
        tmp=rk[i+3]; rk[i+3]=rk[j+3]; rk[j+3]=tmp;
    }
    /* InvMixColumns on all but first and last round keys */
    for(i=1; i<Nr; i++) {
        rk += 4;
        uint8_t *b = (uint8_t*)rk;
        for(int col=0; col<4; col++,b+=4) {
            uint8_t s0=b[0],s1=b[1],s2=b[2],s3=b[3];
            b[0]=MUL(s0,0xe)^MUL(s1,0xb)^MUL(s2,0xd)^MUL(s3,0x9);
            b[1]=MUL(s0,0x9)^MUL(s1,0xe)^MUL(s2,0xb)^MUL(s3,0xd);
            b[2]=MUL(s0,0xd)^MUL(s1,0x9)^MUL(s2,0xe)^MUL(s3,0xb);
            b[3]=MUL(s0,0xb)^MUL(s1,0xd)^MUL(s2,0x9)^MUL(s3,0xe);
        }
    }
    return Nr;
}

void rijndaelEncrypt(const uint32_t rk[], int Nr, const uint8_t pt[16], uint8_t ct[16]) {
    uint8_t s[16], t[16]; int r;
    memcpy(s, pt, 16);
    for(int i=0;i<16;i++) s[i]^=((uint8_t*)rk)[i];
    for(r=1; r<Nr; r++) {
        rk+=4;
        for(int c=0;c<4;c++){
            int i=c*4;
            uint8_t s0=s[i],s1=s[i+1],s2=s[i+2],s3=s[i+3];
            /* This inner loop is intentionally simple for clarity */
            (void)s0;(void)s1;(void)s2;(void)s3;
        }
        /* Use FSb + MixColumns inline */
        for(int c=0;c<4;c++){
            uint8_t a=FSb[s[0+c]],b=FSb[s[4+((c+1)%4)]],
                    d=FSb[s[8+((c+2)%4)]],e=FSb[s[12+((c+3)%4)]];
            t[c*4+0]=XTIME(a)^(XTIME(b)^b)^d^e;
            t[c*4+1]=a^XTIME(b)^(XTIME(d)^d)^e;
            t[c*4+2]=a^b^XTIME(d)^(XTIME(e)^e);
            t[c*4+3]=(XTIME(a)^a)^b^d^XTIME(e);
        }
        uint8_t *rk8=(uint8_t*)(rk);
        for(int i=0;i<16;i++) s[i]=t[i]^rk8[i];
    }
    /* Final round (no MixColumns) */
    rk+=4; uint8_t *rk8=(uint8_t*)rk;
    ct[0] =FSb[s[0]] ^rk8[0];  ct[1] =FSb[s[5]] ^rk8[1];
    ct[2] =FSb[s[10]]^rk8[2];  ct[3] =FSb[s[15]]^rk8[3];
    ct[4] =FSb[s[4]] ^rk8[4];  ct[5] =FSb[s[9]] ^rk8[5];
    ct[6] =FSb[s[14]]^rk8[6];  ct[7] =FSb[s[3]] ^rk8[7];
    ct[8] =FSb[s[8]] ^rk8[8];  ct[9] =FSb[s[13]]^rk8[9];
    ct[10]=FSb[s[2]] ^rk8[10]; ct[11]=FSb[s[7]] ^rk8[11];
    ct[12]=FSb[s[12]]^rk8[12]; ct[13]=FSb[s[1]] ^rk8[13];
    ct[14]=FSb[s[6]] ^rk8[14]; ct[15]=FSb[s[11]]^rk8[15];
}

void rijndaelDecrypt(const uint32_t rk[], int Nr, const uint8_t ct[16], uint8_t pt[16]) {
    uint8_t s[16], t[16];
    memcpy(s, ct, 16);
    for(int i=0;i<16;i++) s[i]^=((uint8_t*)rk)[i];
    for(int r=1; r<Nr; r++){
        rk+=4; uint8_t *rk8=(uint8_t*)rk;
        /* InvShiftRows + InvSubBytes */
        t[0] =RSb[s[0]];  t[1] =RSb[s[13]]; t[2] =RSb[s[10]]; t[3] =RSb[s[7]];
        t[4] =RSb[s[4]];  t[5] =RSb[s[1]];  t[6] =RSb[s[14]]; t[7] =RSb[s[11]];
        t[8] =RSb[s[8]];  t[9] =RSb[s[5]];  t[10]=RSb[s[2]];  t[11]=RSb[s[15]];
        t[12]=RSb[s[12]]; t[13]=RSb[s[9]];  t[14]=RSb[s[6]];  t[15]=RSb[s[3]];
        /* InvMixColumns */
        for(int c=0;c<4;c++){
            uint8_t a=t[c*4],b=t[c*4+1],d=t[c*4+2],e=t[c*4+3];
            s[c*4+0]=MUL(a,0xe)^MUL(b,0xb)^MUL(d,0xd)^MUL(e,0x9);
            s[c*4+1]=MUL(a,0x9)^MUL(b,0xe)^MUL(d,0xb)^MUL(e,0xd);
            s[c*4+2]=MUL(a,0xd)^MUL(b,0x9)^MUL(d,0xe)^MUL(e,0xb);
            s[c*4+3]=MUL(a,0xb)^MUL(b,0xd)^MUL(d,0x9)^MUL(e,0xe);
        }
        for(int i=0;i<16;i++) s[i]^=rk8[i];
    }
    rk+=4; uint8_t *rk8=(uint8_t*)rk;
    pt[0] =RSb[s[0]] ^rk8[0];  pt[1] =RSb[s[13]]^rk8[1];
    pt[2] =RSb[s[10]]^rk8[2];  pt[3] =RSb[s[7]] ^rk8[3];
    pt[4] =RSb[s[4]] ^rk8[4];  pt[5] =RSb[s[1]] ^rk8[5];
    pt[6] =RSb[s[14]]^rk8[6];  pt[7] =RSb[s[11]]^rk8[7];
    pt[8] =RSb[s[8]] ^rk8[8];  pt[9] =RSb[s[5]] ^rk8[9];
    pt[10]=RSb[s[2]] ^rk8[10]; pt[11]=RSb[s[15]]^rk8[11];
    pt[12]=RSb[s[12]]^rk8[12]; pt[13]=RSb[s[9]] ^rk8[13];
    pt[14]=RSb[s[6]] ^rk8[14]; pt[15]=RSb[s[3]] ^rk8[15];
}
