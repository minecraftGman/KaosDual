#define GETU32(p) ( ((uint32_t)(p)[0]<<24) ^ ((uint32_t)(p)[1]<<16) \
                  ^ ((uint32_t)(p)[2]<< 8) ^ ((uint32_t)(p)[3]) )
#define PUTU32(ct,st) { (ct)[0]=(uint8_t)((st)>>24); (ct)[1]=(uint8_t)((st)>>16); \
                        (ct)[2]=(uint8_t)((st)>>8);  (ct)[3]=(uint8_t)(st); }

